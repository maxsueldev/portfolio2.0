					<p class="recuo">Se analisarmos nosso cotidiano e todas as questões econômicas que o Brasil está enfrentando, perceberemos que está se tornando cada vez mais comum vermos novos negócios nascendo e mais pessoas começando a investir no mercado empreendedor. O problema de todo esse crescimento é que está surgindo com ele uma estatística bem desagradável de comentar: <strong>o fechamento de muitas empresas!</strong></p>

					<p class="recuo">Gerir um negócio não é tarefa fácil e se não houver um bom planejamento, um investimento em <i>marketing</i> e produtos que realmente satisfaçam a necessidade do cliente, a simples intenção de possuir uma renda própria (<i>ou extra</i>) torna-se uma avalanche de problemas e preocupações.</p>

					<p class="recuo">Pensando nisso, separei algumas dicas de extrema importância tanto para quem está abrindo seu primeiro negócio, quanto para quem deseja investir em algo novo ou mudar um pouco o que está oferecendo. Vamos lá?</p>

					<p><h2 class="subtitulo">1º. Colocando a ideia no papel</h2></p>
					
					<img src="../img/artigos/como-abrir-meu-primeiro-negocio/ideia-papel.jpg" alt="ideia-papel">

					<p class="recuo">É interessante que todo empreendedor compreenda que ideia não é negócio enquanto não houver planejamento, ou enquanto não houver sequer por escrito aquilo que pretende ser feito. Colocar a ideia no papel é uma forma de você analisar e visualizar qual é realmente seu negócio, quem poderá estar interessado no produto ou serviço que você está oferecendo, quais são os seus principais concorrentes, o que você pode fazer para mudar ou para satisfazer a necessidade de um mercado, como inovar na forma de oferecimento daquele produto e várias outras coisas.</p>

					<p class="recuo">Possuir autoconhecimento e estar ciente de que esse é o momento de entender seu mercado é o principal ponto para dar o pontapé e deslanchar no seu novo investimento, afinal de contas, como vou apresentar meu produto ou serviço para um potencial cliente ou potencial parceiro se não conseguir ao menos apresentar essa ideia para um papel?</p>

					<p><h2 class="subtitulo">2º. Analisando meu mercado e validando minha ideia!</h2></p>

					<img src="../img/artigos/como-abrir-meu-primeiro-negocio/validar-ideia.jpg" alt="validar-ideia">

					<p class="recuo">Falar que é necessário conhecer seu mercado seria besteira se realmente essa fosse uma tarefa simples, mas bem ao contrário do que parece, esse é um dos pontos cruciais para o sucesso de um empreendimento. É imprescindível entendermos que cliente é diferente de usuário e que cada cabeça é um mundo, ou seja, não vale classificar cliente como: <i>"Jovens de 18 a 25 Anos"</i>, porque entre essa categoria de idade existe muita gente e muita diferença.</p>

					<p class="citacao">“É importante que ao definir qual é o seu mercado você seja o mais claro possível, por exemplo: Eu tenho uma empresa que fabrica camisas de algodão com estampas de profissões, então meu mercado será de homens e mulheres, maiores de 17 anos, que estejam se graduando em cursos referentes as estampas que ofereço, dessa forma estarei delimitando meu mercado e assim poderei dar um foco maior àqueles que são meus possíveis futuros compradores.”</p>

					<p class="recuo">Assim como é difícil definir um mercado, também é bastante complicado validar se uma ideia realmente pode se transformar num negócio de sucesso, e, se existe melhor forma de validação que a conversa ainda preciso conhecê-la!</p>

					<p class="recuo">Converse sobre seu negócio com pessoas que estejam interessadas naquilo que você vai oferecer (<i>não vale falar somente para a família e considerar isso uma validação</i>), faça pesquisas, analise quais são seus pontos fortes e o que é preciso mudar, entenda quem utilizaria aquilo que você oferece, quem pagaria por isso, quem indicaria você para outras pessoas e assim por diante e não esqueça: <strong style="font-style: italic;">aceite opiniões e entenda que nem sempre sua primeira ideia é aquela que vai ao mercado, a palavra do momento é pivotar, começar de novo, e ela precisa ser executada, contudo conheça suficientemente seu mercado para entender o que precisa ou não ser mudado, muitas vezes durante as pesquisas as opiniões de seus clientes se chocam e cabe a você ter o voto de minerva!</strong></p>

					<p class="citacao">"Mostre-se para o mercado e deixe o mercado mostrar-se para você!"</p>

					<p><h2 class="subtitulo">3º. A importância do MVP (Minimum Viable Product)</h2></p>

					<img src="../img/artigos/como-abrir-meu-primeiro-negocio/mvp.jpg" alt="MVP">
					
					<p class="recuo">É extremamente claro que uma das coisas mais importantes para a abertura do seu negócio é acreditar que ele vai dar certo, porém, precisa-se de cuidado no momento de iniciar para que não haja nenhum tipo de prejuízo, não é mesmo?</p>

					<p class="recuo">Para isso existe uma coisa chamada de "<i>Minimum Viable Product</i>" (em português, Mínimo Produto Viável ou MPV) que nada mais é do que o ato de lançar primeiro para o mercado aquilo que é essencial para o bom funcionamento do seu negócio e com o passar do tempo e o avançar do seu trabalho ir aprimorando sua propriedade. Essa dica serve para tudo, seja para negócio físico ou online! (<i>Se você já viu algum aplicativo com o nome Beta ou Beta Test é exatamente por isso!</i>)</p>

					<p class="citacao">“Lembrem da dica dos nossos avós: Devagar e sempre!”</p>

					<p><h2 class="subtitulo">4º. Publicidade: sua melhor e pior arma!</h2></p>

					<img src="../img/artigos/como-abrir-meu-primeiro-negocio/publicidade.jpg" alt="publicidade">

					<p class="recuo">Quando dizem que publicidade é a alma do negócio estão completamente certos, mas devemos estar cientes que é necessária uma boa publicidade para que ela seja proveitosa (<i>trabalho meia boca nem pensar!</i>), e, não deixe de ser a alma do negócio para ser uma arma contra você. A internet está aqui para ser utilizada e se tem um lugar no mundo para uma ótima publicidade a baixo custo é ela, entretanto, antes de fazer a publicidade de sua marca, é preciso lembrar onde estão seus clientes e qual o melhor canal de acesso a eles. <strong>A internet não é a única ferramenta de comunicação!</strong></p>

					<p><h2 class="subtitulo">5º. Existem ferramentas preciosas no mundo... </h2></p>

					<img src="../img/artigos/como-abrir-meu-primeiro-negocio/canvas.jpg" alt="canvas">

					<p class="recuo">É claro que analisar um mercado é complicado, que validar uma ideia é essencial e trabalhoso, que conhecer seu produto e serviço é uma tarefa de um tamanho impossível de mensurar e é exatamente por isso que com o decorrer do tempo pessoas incríveis lançaram muitas ferramentas (<i>ou métodos, como queiram chamar</i>) que nos ajudam muito nesse momento de iniciação de uma empresa e se vamos falar de ferramentas, não podemos deixar de falar sobre o CANVAS!</p>

					<p class="recuo">O <i>Canvas Business Model</i>, por exemplo, é um modelo de Mapa Visual desenvolvido como espécie de “resumão” de um plano de negócios que aborda, através de perguntas, os quatro pontos mais importantes da sua empresa: <strong>"Como vou fazer, O que vou fazer, Para quem estou fazendo e Quanto vou gastar e ganhar?"</strong>. Essa ferramenta é de grande valor sempre que é preciso tomar decisões e se você quer garantir o bom funcionamento da sua empresa sempre o mantenha atualizado!</p>

					<p class="recuo">Ta aí uma ferramenta que muda <strong>CONSTANTEMENTE</strong> e que com certeza lhe dará um excelente entendimento do negócio que tem em mãos, já que está sempre de acordo com sua situação atual! <!-- Mas não pense que acabou por aí, conheça um pouco mais dessa ferramenta e de outras <a href="#">clicando aqui!</a> --></p>

