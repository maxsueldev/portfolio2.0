					<p class="recuo">Eu sempre fui o tipo de aluna super questionadora que sempre buscava entender porque eu queria ou porque eu devia aprender algo, não que algum aprendizado fosse irrelevante, mas eu gostava de me dedicar a aquilo que eu acreditava que fosse ser um diferencial para mim, e o inglês por muitos anos não fez parte dessa lista, até que tive minha primeira decepção: fui eliminada da entrevista do emprego dos meus sonhos por não saber falar inglês!</p>

					<p class="citacao">Ao contrário do que se parece essa situação é bastante comum e acontece todos os dias com algum de nós brasileiros e, por mais que você não acredite, isso não é um clichê, porque amigo, se você não fala inglês um dia você vai sentir uma baita necessidade de ter ouvido o conselho desse texto e correr para aprender!</p>			

					<p class="recuo">Por isso, e por diversos outros tapas que levei da minha vida, fiz uma lista para vocês com as razões mais importantes para se aprender a falar outra língua (falo aqui do inglês porque essa língua em questão é essencial para mim e, obviamente, ela é a linguagem universal) no mundo profissional!</p>

					<p><h2 class="subtitulo">1º. Mercado de trabalho</h2></p>		

					<p class="recuo">Vou começar falando sobre esse ponto porque é o que mais me incomoda e o que eu mais sofri. Estamos na era da globalização e por mais que o nosso país esteja passando por sérios problemas econômicos e políticos ele não está parado e nem pretende parar.</p>

					<p class="recuo">Temos no Brasil diversas empresas multinacionais em funcionamento, empresas brasileiras (até mesmo de médio e pequeno porte) que atendem clientes fora do país, empresas que prestam serviços terceirizados para empresas que atendem clientes internacionais e mais ainda, empresas que recebem treinamentos de empresas de outros países. Viram quantas vezes só nesse trechinho precisamos falar e entender o inglês?</p>

					<p class="recuo">O não entendimento da língua reduz a quantidade de vagas disponível para você, enquanto o entendimento te abre novos horizontes e novas chances de atingir novos mercados, muitas vezes trazendo melhores salários, melhores formações, melhores benefícios e mais ainda dando a você diferenciais competitivos diante do seu conhecimento na sua área de atuação.</p>

					<p><h2 class="subtitulo">2º. Formação acadêmica</h2></p>

					<p class="recuo">Se você assim como eu passa horas escolhendo um curso aqui no Brasil, imagina com a possibilidade de escolher cursos aqui e fora daqui. Claro, nosso país é cheio de bagagem e conhecimento para nos proporcionar, mas saber falar inglês nos dá a oportunidade de fazer intercâmbios, ler outros artigos de assuntos que as vezes ainda não foram escritos em português, ler mais livros e muitas outras coisas.</p>

					<p class="recuo">O conhecimento de uma nova língua nos dá a chance de realmente entender o significado da palavra MULTIPLICAR, sejam conhecimentos e até mesmo momentos super proveitosos, afinal de contas num intercâmbio, por exemplo, além de aprendermos muito, conhecemos novas culturas, novas pessoas, novos lugares e aproveitamos para curtir viagens extremamente gostosas de se fazer!</p>

					<p><h2 class="subtitulo">3º. Mundo dos negócios</h2></p>

					<p class="recuo">Saber falar inglês nos ajuda bastante a encontrar melhores vagas de emprego, mas não é somente nesse momento da busca que a língua nos ajuda, é no dia a dia também.</p>

					<p class="recuo">Ter conhecimento sobre uma nova língua nos dá uma maior autoconfiança já que nos sentimos seguros para falar com quem tiver que seja, uma maior oportunidade de fazer <i>networking</i> em momentos como palestras e treinamentos que frequentamos durante nosso trajeto profissional, abre portas para novos desafios dentro da empresa, seja um atendimento a um cliente especial ou a oportunidade de ministrar uma palestra para clientes internacionais, além de nos dar acesso a maiores portais de conhecimento e mais pessoas que podem ser importantes no decorrer da nossa carreira.</p>

					<p><h2 class="subtitulo">4º. E por fim, mas não menos importante, Internacionalização</h2></p>

					<p class="recuo">A palavra é difícil, mas seu significado é bem simples: Falar inglês nos dá abertura de trabalhar onde quisermos, com quem quisermos e na empresa que quisermos porque a língua nos faz internacionais, não no sentido de deixarmos de ser brasileiros porque minha pátria faz parte de quem eu sou, mas no sentido de ter liberdade para trabalhar aqui no Brasil, na China ou na África do Sul não tendo a preocupação de que vão lhe entender e se você entenderá as pessoas porque isso já será uma coisa simples para você!</p>

